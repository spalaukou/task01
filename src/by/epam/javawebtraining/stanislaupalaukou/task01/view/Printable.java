/**
 * Interface Printable defines the print method.
 *
 * @author Stanislau Palaukou on 17.02.2019
 * @project Task 01
 */

public interface Printable {

    /**
     * Classes should implement the method to print Object,
     * which is received as param.
     *
     * @param o will be printed
     */
    void print(Object o);
}
